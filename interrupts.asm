.INCLUDE "M328Pdef.inc"

.ORG 0
	jmp MAIN
.ORG 0x02
	JMP EX0_ISR
.ORG 0x04
	JMP EX1_ISR

LDI R16,LOW(RAMEND)
OUT SPL,R16
LDI R16,HIGH(RAMEND)
OUT SPH,R16

MAIN: 
LDI R16,0XFF
OUT DDRB,R16
LDI R16,0X00

LDI R17,0b00000011
OUT EIFR,R17
OUT EIMSK,R17
LDI R18,0b00001010
sts EICRA,R18
SEI
here: jmp here

EX0_ISR:
	INC R16
	OUT PORTB,R16
	RETI
EX1_ISR:
	LDI R16,0
	OUT PORTB,R16
	RETI
